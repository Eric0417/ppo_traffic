print("開始匹配路口 ID...\n" + "-"*40)

mapping_result = {}
search_radius = 50  # 搜索半徑 (米)

# 3. 遍歷每一個地點進行匹配
for name, coords in locations.items():
    lat, lon = coords
    
    # 將經緯度轉換為 SUMO 內部的 X, Y 座標
    x, y = net.convertLonLat2XY(lon, lat)
    
    # 【修正部分】手動計算距離，找出半徑內的所有節點
    nearby_nodes = []
    for node in net.getNodes():
        nx, ny = node.getCoord() # 取得該路口的 X,Y 座標
        # 計算兩點之間的直線距離 (歐式距離)
        dist = math.hypot(nx - x, ny - y) 
        if dist <= search_radius:
            nearby_nodes.append((node, dist))
            
    # 依照距離由近到遠排序
    nearby_nodes.sort(key=lambda item: item[1])
    
    if nearby_nodes:
        # 優先尋找類型是 "traffic_light" (有紅綠燈) 的路口
        tl_nodes = [n for n in nearby_nodes if n[0].getType() == 'traffic_light']
        
        if tl_nodes:
            best_node = tl_nodes[0][0]
            node_type = "紅綠燈路口"
        else:
            # 如果附近沒有紅綠燈，就抓最近的普通路口
            best_node = nearby_nodes[0][0]
            node_type = "普通路口"
            
        node_id = best_node.getID()
        mapping_result[name] = node_id
        
        print(f"✅ {name}")
        print(f"   -> 找到 SUMO ID: {node_id} ({node_type})")
    else:
        print(f"❌ {name}")
        print(f"   -> 警告：半徑 {search_radius} 米內找不到任何路口！")

